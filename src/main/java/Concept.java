import java.util.List;

class Concept {
    private List<Integer> objectSet;
    private List<Integer> attributeSet;

    public List<Integer> getObjectSet() {
        return objectSet;
    }

    public void setObjectSet(List<Integer> objectSet) {
        this.objectSet = objectSet;
    }

    public List<Integer> getAttributeSet() {
        return attributeSet;
    }

    public void setAttributeSet(List<Integer> attributeSet) {
        this.attributeSet = attributeSet;
    }
}